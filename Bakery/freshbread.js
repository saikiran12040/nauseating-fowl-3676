  let count=0
  const appendData=(data,container)=>{
    container.innerHTML=null;
    data.map((el,i)=>{
        count++;
        let div=document.createElement('div');
        let image=document.createElement('img');
        image.src=el.image_url;
       
        let name=document.createElement('p');
        name.innerText=el.product_name;
        let piece=document.createElement('p');
        piece.innerText=`${el.piece} Pc`
        let price=document.createElement('span');
        price.innerText=`MRP ${el.product_price}Rs `
        let btn=document.createElement('button');
        btn.style.cursor='pointer'
        btn.innerText='ADD'
        image.addEventListener('click',()=>{
            let productData={
            image:el.image_url,
             name:el.product_name,
             piece:el.piece,
             price:el.product_price,
            }
            localStorage.setItem('details',JSON.stringify(productData));
            window.location.href='productPage.html'
        })
        btn.addEventListener('click',()=>{
           let  CartData=JSON.parse(localStorage.getItem('products'))||[];
           let obj={
             image:el.image_url,
             name:el.product_name,
             piece:el.piece,
             price:el.product_price,
           }
           CartData.push(obj);
           localStorage.setItem('products',JSON.stringify(CartData));
           
       
    });
        
    div.append(image,name,piece,price,btn);
    container.append(div);
  })
  document.querySelector('#headline>div>p:last-child').innerText=`${count} Products`;
  }

  

  let Productcontainer=document.querySelector('#appendData')

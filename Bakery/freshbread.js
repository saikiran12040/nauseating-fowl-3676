  let priceSort=document.querySelector('#sortBakery_');
  priceSort.addEventListener('change',handleSortPrice);

  function handleSortPrice(){
    if(priceSort.value==='Relevence'){
        window.location.href='freshbread.html'
    }
    else if(priceSort.value==='Low to High price'){
        bakeryData.sort((a,b)=>{
           return (a.product_price)-(b.product_price);
        })
        appendData(bakeryData,Productcontainer)
      
        }else if( priceSort.value==='High to Low price'){
            bakeryData.sort((a,b)=>{
                return (b.product_price)-(a.product_price);
            });
            
            appendData(bakeryData,Productcontainer)
                 }
  }

 
 

  